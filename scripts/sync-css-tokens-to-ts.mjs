#!/usr/bin/env node
/**
 * Parses tokens.css and generates themeColors.generated.ts so canvas/SVG/chart
 * code can access design tokens as typed hex constants without manual sync.
 *
 * Run:  pnpm tokens:sync
 * CI:   pnpm tokens:check  (runs sync then git diff --exit-code)
 */
import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));

const TOKENS_CSS = resolve(
  __dirname,
  "../packages/dbt-tools/web/src/styles/tokens.css",
);
const OUTPUT_TS = resolve(
  __dirname,
  "../packages/dbt-tools/web/src/constants/themeColors.generated.ts",
);

/** Extract all `--name: value;` definitions from a CSS block string. */
function parseVarDefinitions(block) {
  /** @type {Record<string, string>} */
  const tokens = {};
  for (const line of block.split("\n")) {
    const m = line.match(/^\s*--([\w-]+)\s*:\s*(.+?)\s*;/);
    if (m) tokens[`--${m[1]}`] = m[2];
  }
  return tokens;
}

/** Extract the content between `{` and `}` for a given selector regex. */
function extractBlock(css, selectorRe) {
  const match = css.match(selectorRe);
  if (!match) return "";
  const start = css.indexOf("{", match.index) + 1;
  let depth = 1;
  let i = start;
  while (i < css.length && depth > 0) {
    if (css[i] === "{") depth++;
    else if (css[i] === "}") depth--;
    i++;
  }
  return css.slice(start, i - 1);
}

/** Resolve one level of `var(--name)` references within the same token map. */
function resolveRefs(tokens) {
  /** @type {Record<string, string>} */
  const resolved = {};
  for (const [name, value] of Object.entries(tokens)) {
    const ref = value.match(/^var\(--([\w-]+)\)$/);
    if (ref && tokens[`--${ref[1]}`]) {
      resolved[name] = tokens[`--${ref[1]}`];
    } else {
      resolved[name] = value;
    }
  }
  return resolved;
}

/** Convert `--foo-bar-baz` to `fooBarBaz`. */
function toCamelCase(cssVar) {
  return cssVar
    .replace(/^--/, "")
    .replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase());
}

/**
 * Keep only tokens whose CSS name starts with one of the given prefixes.
 * Returns a camelCase-keyed object.
 */
function filterByPrefix(tokens, prefixes) {
  /** @type {Record<string, string>} */
  const out = {};
  for (const [name, value] of Object.entries(tokens)) {
    if (prefixes.some((p) => name.startsWith(p))) {
      out[toCamelCase(name)] = value;
    }
  }
  return out;
}

const KEEP_PREFIXES = [
  "--bg-",
  "--border-",
  "--text-primary",
  "--text-secondary",
  "--text-tertiary",
  "--text-inverse",
  "--text-accent",
  "--text-success",
  "--text-warning",
  "--text-danger",
  "--text-info",
  "--text-disabled",
  "--icon-",
  "--accent-",
  "--success-",
  "--warning-",
  "--danger-",
  "--info-",
  "--chart-",
  "--dbt-type-",
  "--graph-",
];

const css = readFileSync(TOKENS_CSS, "utf-8");

const lightBlock = extractBlock(css, /:root\s*\{/);
const darkBlock = extractBlock(css, /\[data-theme="dark"\]\s*\{/);

const lightRaw = parseVarDefinitions(lightBlock);
const darkRaw = parseVarDefinitions(darkBlock);

const light = resolveRefs(lightRaw);
const dark = resolveRefs(darkRaw);

const lightFiltered = filterByPrefix(light, KEEP_PREFIXES);
const darkFiltered = filterByPrefix(dark, KEEP_PREFIXES);

const jsonLight = JSON.stringify(lightFiltered, null, 2);
const jsonDark = JSON.stringify(darkFiltered, null, 2);

const output = `// AUTO-GENERATED by scripts/sync-css-tokens-to-ts.mjs — do not edit manually.
// Run \`pnpm tokens:sync\` after changing tokens.css.

export type ThemeMode = "light" | "dark";

export const TOKENS_LIGHT = ${jsonLight} as const;

export const TOKENS_DARK = ${jsonDark} as const;

export type TokenName = keyof typeof TOKENS_LIGHT;

export function getToken(
  name: TokenName,
  theme: ThemeMode,
): string {
  return theme === "dark"
    ? (TOKENS_DARK as Record<string, string>)[name] ?? TOKENS_LIGHT[name]
    : TOKENS_LIGHT[name];
}
`;

writeFileSync(OUTPUT_TS, output);
console.log(`✓ Generated ${OUTPUT_TS}`);
